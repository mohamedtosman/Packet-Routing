package view;


public class NextXYNumPackets {
	int xNext;
	int yNext;
	int numPackets;
	
	public int getxNext() {
		return xNext;
	}
	public void setxNext(int xNext) {
		this.xNext = xNext;
	}
	public int getyNext() {
		return yNext;
	}
	public void setyNext(int yNext) {
		this.yNext = yNext;
	}
	public int getNumPackets() {
		return numPackets;
	}
	public void setNumPackets(int numPackets) {
		this.numPackets = numPackets;
	}	
	
}
